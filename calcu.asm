.MODEL SMALL
.STACK 100
.DATA
	ORDERQTY DB 5
	ORDERPRICE DB "12.50$"
	ORDERTOTAL DW  ?
	ORDERCENTS DB ?
	
	
	TEMPC DB ?
	TEN DB 10
	TWO DB 2
	HUNDRED DB 100
.CODE
MAIN PROC
	MOV AX, @DATA
	MOV DS, AX
	
	SUB ORDERPRICE[0], 30H
	MOV AL, ORDERPRICE[0]
	MUL TEN
	MOV TEMPC, AL
	
	SUB ORDERPRICE[1], 30H
	MOV AL, ORDERPRICE[1]
	ADD TEMPC, AL
	
	MOV AL, TEMPC
	MUL ORDERQTY
	MOV ORDERTOTAL, AX
;-------------------------------------------------
	SUB ORDERPRICE[3], 30H
	MOV AL, ORDERPRICE[3]
	MUL TEN
	MOV TEMPC, AL
	
	SUB ORDERPRICE[4], 30H
	MOV AL, ORDERPRICE[4]
	ADD TEMPC, AL
	
	MOV AL, TEMPC
	MUL ORDERQTY
	DIV HUNDRED
	CMP AL, 1
	JE ADD1TOORDER
	MOV ORDERCENTS, AH
	JMP DISPLAY
	
DISPLAY:
	MOV AX, ORDERTOTAL
	DIV TEN
	MOV BX, AX
		
	MOV AH, 02H
	MOV DL, BL
	ADD DL, 30H
	INT 21H
	
	MOV AH, 02H
	MOV DL, BH
	ADD DL, 30H
	INT 21H

	MOV AH, 02H
	MOV DL, '.'
	INT 21H
	
	MOV AH, 0
	MOV AL, ORDERCENTS
	DIV TEN
	MOV BX, AX
	
	MOV AH, 02H
	MOV DL, BL
	ADD DL, 30H
	INT 21H
	
	MOV AH, 02H
	MOV DL, BH
	ADD DL, 30H
	INT 21H
	
	JMP FIN
	
ADD1TOORDER:
	ADD ORDERTOTAL, 1
	MOV ORDERCENTS, AH
	JMP DISPLAY
FIN:
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN